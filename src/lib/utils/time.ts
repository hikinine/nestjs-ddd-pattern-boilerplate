export const seconds = (value: number) => value * 1000;
export const minutes = (value: number) => value * 60 * 1000;
export const hours = (value: number) => value * 60 * 60 * 1000;
export const days = (value: number) => value * 24 * 60 * 60 * 1000;
export const weeks = (value: number) => value * 7 * 24 * 60 * 60 * 1000;
export const months = (value: number) => value * 30 * 24 * 60 * 60 * 1000;
export const years = (value: number) => value * 365 * 24 * 60 * 60 * 1000;
